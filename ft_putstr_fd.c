#include "printf.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	//int nb;
	
	i = 0;
	//nb = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i ++;
		//nb ++;
	}
	//return(nb);
}